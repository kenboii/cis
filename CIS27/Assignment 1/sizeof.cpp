#include <iostream>
using namespace std;

int main ()
{
    cout << "Byte size of char is: " << sizeof(char) << endl;
    cout << "Byte size of int is: " <<sizeof(int) << endl;
    cout << "Byte size of short is: " <<sizeof(short) << endl;
    cout << "Byte size of long is: " <<sizeof(long) << endl;
    cout << "Byte size of float is: " <<sizeof(float) << endl;
    cout << "Byte size of double is: " <<sizeof(double) << endl;
    cout << "Byte size of bool is: " <<sizeof(bool) << endl;

    return 0;
}